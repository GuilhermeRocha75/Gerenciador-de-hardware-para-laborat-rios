
package br.com.DTO;


public class PecasManutrencaoDTO {
 
      private int idPecaManutencao, idPeca, idManutencao;
      private String quantidade;

    public int getIdPecaManutencao() {
        return idPecaManutencao;
    }

    public void setIdPecaManutencao(int idPecaManutencao) {
        this.idPecaManutencao = idPecaManutencao;
    }

    public int getIdPeca() {
        return idPeca;
    }

    public void setIdPeca(int idPeca) {
        this.idPeca = idPeca;
    }

    public int getIdManutencao() {
        return idManutencao;
    }

    public void setIdManutencao(int idManutencao) {
        this.idManutencao = idManutencao;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }
      
      
    
    
}
